import tkinter as tk
from tkinter import *

class LoginScreen():
  
  def __init__(self):
    self.root = tk.Tk()
    self.run
    self.root.mainloop()
  
  def run(self):
    self.root.title("Login")
    self.root.geometry("1000x1200")
    self.root.configure(background="gray")
    self.root.resizable(True, True)

